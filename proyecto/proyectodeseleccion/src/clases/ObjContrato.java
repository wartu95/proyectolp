package clases;

public class ObjContrato {

	private int idObjContrato;
	private String desObjContrato;
	
	
	public ObjContrato(int idObjContrato, String desObjContrato) {
		
		this.idObjContrato = idObjContrato;;
		this.desObjContrato = desObjContrato;
	}


	public int getIdObjContato() {
		return idObjContrato;
	}


	public void setIdObjContrato(int idObjContrato) {
		this.idObjContrato = idObjContrato;
	}


	public String getDesObjContrato() {
		return desObjContrato;
	}


	public void setDesObjContrato(String desObjContrato) {
		this.desObjContrato = desObjContrato;
	}
	
	
}
